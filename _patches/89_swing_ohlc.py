    '    # Patch 89: Kraken pair map — same as SCALP/MID scanners\n'
    '    _KRAKEN_PAIRS = {\n'
    '        "SOL": ("SOLUSD",  "SOLUSD"),\n'
    '        "BTC": ("XBTUSD",  "XXBTZUSD"),\n'
