    '    }\n'
    '\n'
    '    while True:\n'
    '        await asyncio.sleep(120)  # 2 min\n'
    '        try:\n'
    '            perp_enabled = os.getenv("PERP_EXECUTOR_ENABLED", "false").lower() == "true"\n'
    '            if not perp_enabled:\n'
    '                continue\n'
    '\n'
    '            # 4h threshold — 0.5% default (larger move on 4h candle vs 5m/15m)\n'
    '            try:\n'
    '                threshold = float(os.getenv("SWING_4H_THRESHOLD", "0.5"))\n'
    '            except Exception:\n'
    '                threshold = 0.5\n'
    '\n'
    '            import requests as _req\n'
    '\n'
    '            try:\n'
    '                from utils.market_cycle import get_cycle_phase  # type: ignore\n'
    '                phase = get_cycle_phase()\n'
